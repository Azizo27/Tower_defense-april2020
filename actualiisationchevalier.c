void actualiserListeChevalier(t_listeChevaliers * la, BITMAP * caseChevalier, BITMAP* casecolallie)
{
    int i;
    for(i=0; i<la->max; i++)
    {
        if((la->tab[i])!=NULL)
        {


            if(!(la->tab[i]->vivant))
            {
                enleverChevalier(la,i,caseChevalier, casecolallie);
            }
        }
    }
}

